#include <iostream>
using namespace std;
int main()
{
                  int n;
                  cout << n;
                  int i = 2;
                  while (i < n)
                  {
                                    if (n % i == 0)
                                    {
                                                      cout << "not prime for" << i << endl;
                                    }
                                    else
                                    {
                                                      cout << "it is prime" << endl;
                                    }
                                    i = i + 1;
                  }
}